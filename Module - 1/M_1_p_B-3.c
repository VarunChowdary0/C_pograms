/*
Compilers, assemblers, and interpreters are all tools used in the process 
of translating high-level programming languages into machine-executable code. 
However, they serve different roles in this translation process. Let's 
differentiate among these three:

    1. Compiler:
        A compiler is a software tool that translates the entire source code of a 
        high-level programming language into machine code or an equivalent form 
        known as object code. The translation is done in a single step, and the 
        resulting object code can be executed directly by the computer's CPU.

        Examples: GCC (GNU Compiler Collection) for C/C++, Visual C++ Compiler, Java Compiler.
    
    2. Assembler:
        An assembler is a tool used to translate assembly language code into 
        machine code. Assembly language is a low-level programming language 
        that uses mnemonic codes to represent machine-level instructions. 
        The assembler performs a one-to-one translation of assembly instructions 
        into their corresponding machine-level representations.

    3. Interpreter:
        An interpreter is a program that translates and executes source code line by line, 
        immediately converting each line into machine code and executing it. It doesn't 
        produce a separate compiled output. Instead, it executes the program directly.

        Examples: Python interpreter, JavaScript interpreter.
*/